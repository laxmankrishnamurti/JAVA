public class Patterns {
    public static void main(String[] args) {
        /*
         * Pattern :- 01
         * *
         * * *
         * * * *
         * * * * *
         * * * * * *
         */

        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * * \n \n");

        /*
         * Pattern :- 02
         * * * * * *
         * * * * *
         * * * *
         * * *
         * *
         */

        System.out.println("* * * * *");
        System.out.println("* * * * ");
        System.out.println("* * * ");
        System.out.println("* * ");
        System.out.println("* ");

        /*
         * Pattern :- 03
         * *
         * * *
         * * * *
         * * * * *
         * * * * * *
         */

        System.out.println("        *");
        System.out.println("      * *");
        System.out.println("    * * *");
        System.out.println("  * * * *");
        System.out.println("* * * * *");

    }
}
